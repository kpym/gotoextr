package main

import (
	"testing"
)

func Test_e7toDec(t *testing.T) {
	data := []struct {
		in  IntString
		out string
	}{
		{"0987654321", "098.7654321"},
		{"987654321", "98.7654321"},
		{"87654321", "8.7654321"},
		{"7654321", "0.7654321"},
		{"54321", "0.0054321"},
		{"-0987654321", "-098.7654321"},
		{"-987654321", "-98.7654321"},
		{"-87654321", "-8.7654321"},
		{"-7654321", "-0.7654321"},
		{"-54321", "-0.0054321"},
	}

	for _, d := range data {
		if e7toDec(d.in) != d.out {
			t.Errorf("e7toDec(%s) != %s", d.in, d.out)
		}
	}
}
